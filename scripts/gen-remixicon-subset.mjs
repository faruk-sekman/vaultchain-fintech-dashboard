#!/usr/bin/env node
// Generates Web/src/styles/_remixicon-subset.scss — a subset of remixicon containing ONLY the
// ri-* icons actually used in Web/src. The full remixicon.css is ~154kB (~2800
// icons); the app uses ~100, so the subset cuts ~150kB from the initial CSS bundle. The font file
// (remixicon.woff2) is unchanged — only the CSS class table is trimmed.
//
// Run from the repo root after adding/removing any ri-* icon:
//   node scripts/gen-remixicon-subset.mjs
//
// The output is stylelint-ignored (vendor-derived, generated). Zero dependencies (Node stdlib).
import { readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { join, extname } from 'node:path';

const SRC = 'Web/src';
const REMIX = 'Web/node_modules/remixicon/fonts/remixicon.css';
const OUT = 'Web/src/styles/_remixicon-subset.scss';
const exts = new Set(['.ts', '.html', '.scss']);
const ICON = /\bri-[a-z0-9]+(?:-[a-z0-9]+)*\b/g;

const used = new Set();
(function scan(dir) {
  for (const e of readdirSync(dir, { withFileTypes: true })) {
    const p = join(dir, e.name);
    if (e.isDirectory()) {
      if (e.name !== 'node_modules') scan(p);
    } else if (exts.has(extname(e.name))) {
      for (const m of readFileSync(p, 'utf8').matchAll(ICON)) used.add(m[0]);
    }
  }
})(SRC);

const css = readFileSync(REMIX, 'utf8');
const rule = /\.(ri-[a-z0-9-]+):before\s*\{\s*content:\s*"(\\[0-9a-fA-F]+)";\s*\}/g;
const map = {};
for (const m of css.matchAll(rule)) map[m[1]] = m[2];

const kept = [...used].filter(i => map[i]).sort();
const missing = [...used].filter(i => !map[i]).sort();

const lines = [
  '// GENERATED by scripts/gen-remixicon-subset.mjs — DO NOT EDIT.',
  '// Subset of remixicon (~154kB / ~2800 icons) to ONLY the ri-* icons used in Web/src.',
  '// Re-run the generator after adding/removing icons. stylelint-ignored (vendor-derived).',
  '',
  '@font-face {',
  '  font-family: remixicon;',
  '  src: url("remixicon/fonts/remixicon.woff2") format("woff2");',
  '  font-display: swap;',
  '}',
  '',
  '[class^="ri-"], [class*=" ri-"] {',
  '  font-family: remixicon !important;',
  '  font-style: normal;',
  '  -webkit-font-smoothing: antialiased;',
  '  -moz-osx-font-smoothing: grayscale;',
  '}',
  '',
  ...kept.map(i => `.${i}::before { content: "${map[i]}"; }`),
  '',
];
writeFileSync(OUT, lines.join('\n'));
console.log(
  `remixicon subset: ${kept.length} icons kept, ${missing.length} ignored` +
    (missing.length ? ` (not in remixicon: ${missing.join(', ')})` : ''),
);
